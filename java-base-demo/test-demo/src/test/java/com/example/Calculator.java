public class Calculator {

    public Integer add (Integer a, Integer b){
        return a + b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int divide(int i, int i1) {
        return i / i1;
    }

    public Integer subtract(int i, int i1) {
        return i - i1;
    }
}
